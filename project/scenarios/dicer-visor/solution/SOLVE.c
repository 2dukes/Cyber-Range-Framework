int main() {
  int fd = open("/dev/exploited-device", O_RDWR | O_CLOEXEC);

  write(fd, shellcode, sizeof(shellcode));
  ioctl(fd, 0xdead, NULL);
  ioctl(fd, 0xbeef, NULL);
  __asm__("noop: nop;"
          "jmp noop;");

  return 0;
}
